import type { ModelInfo } from '../types/bridge';

export type Provider =
  | 'anthropic'
  | 'openai'
  | 'google'
  | 'factory'
  | 'meta'
  | 'mistral'
  | 'default';

export function providerOf(model?: ModelInfo, modelId?: string): Provider {
  const hay =
    `${model?.provider ?? ''} ${model?.displayName ?? ''} ${model?.id ?? modelId ?? ''}`.toLowerCase();
  if (/anthropic|claude/.test(hay)) return 'anthropic';
  if (/openai|gpt|codex|\bo1\b|\bo3\b|\bo4\b/.test(hay)) return 'openai';
  if (/google|gemini/.test(hay)) return 'google';
  if (/glm|kimi|deepseek|minimax|qwen|droid core|factory/.test(hay)) return 'factory';
  if (/llama|meta/.test(hay)) return 'meta';
  if (/mistral|mixtral/.test(hay)) return 'mistral';
  return 'default';
}

